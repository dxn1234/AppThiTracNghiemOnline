package com.example.hoangbao.apptracnghiem.model;

public class So {
    String ten;
    int mausac;

    public So() {
    }

    public So(String ten, int mausac) {
        this.ten = ten;
        this.mausac = mausac;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getMausac() {
        return mausac;
    }

    public void setMausac(int mausac) {
        this.mausac = mausac;
    }
}
